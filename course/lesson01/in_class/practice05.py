if __name__ == '__main__':
    # 测试 1: 字符串转整数后交换
    a = "123"
    b = "456"
    a = int(a)
    b = int(b)
    temp = a
    a = b
    b = temp
    print(f"a={a},b={b}")
    assert a == 456
    assert b == 123

    # 测试 2: 两位数交换
    x = "12"
    y = "99"
    x = int(x)
    y = int(y)
    temp = x
    x = y
    y = temp
    print(f"x={x},y={y}")
    assert x == 99
    assert y == 12

    # 测试 3: 含零的字符串交换
    m = "100"
    n = "200"
    m = int(m)
    n = int(n)
    temp = m
    m = n
    n = temp
    print(f"m={m},n={n}")
    assert m == 200
    assert n == 100

