"""
[难度: ⭐⭐⭐]
[所属知识点: 字符串切片、长度与数字判断、格式化输出]
[预计完成时间: 25 分钟]

题目描述:
  许多应用会在显示手机号时隐藏中间几位,保护用户隐私。
  例如:手机号 "13800138000" 脱敏后应显示为 "138****8000"。
  请编写一个程序,对输入的手机号进行脱敏处理。

实现步骤:
  1. 输入一个手机号,用 len() 判断长度是否为 11 位。
  2. 用 isdigit() 判断是否每一位都是数字。
  3. 若同时满足以上两个条件,则该手机号合法:
     - 用切片提取前 3 位(索引 0:3)。
     - 用切片提取后 4 位(索引 -4:)。
     - 中间插入 "****",用 f-string 拼接输出。
  4. 若不符合任一条件,输出 "手机号格式错误"。

示例:
    >>> 输入 "13800138000"
    脱敏结果: 138****8000
    >>> 输入 "13912345678"
    脱敏结果: 139****5678
    >>> 输入 "1380013800"
    手机号格式错误
    >>> 输入 "1380013800a"
    手机号格式错误
"""

# ======================
# 学员代码区(以 pass 作为占位符)
# ======================
phone = "13800138000"
if len(phone) == 11 and phone.isdigit():
    head = phone[0:3]
    tail = phone[-4:]
    result = f"{head}****{tail}"
    print(f"脱敏结果: {result}")
else:
    print("手机号格式错误")

# ======================
# 测试区(教师可复制到终端验证)
# ======================
if __name__ == '__main__':
    # 测试 1: 常规手机号
    phone_1 = "13800138000"
    if len(phone_1) == 11 and phone_1.isdigit():
        head_1 = phone_1[0:3]
        tail_1 = phone_1[-4:]
        result_1 = f"{head_1}****{tail_1}"
        print(f"脱敏结果: {result_1}")
        assert result_1 == "138****8000"

    # 测试 2: 不同号段
    phone_2 = "13912345678"
    if len(phone_2) == 11 and phone_2.isdigit():
        head_2 = phone_2[0:3]
        tail_2 = phone_2[-4:]
        result_2 = f"{head_2}****{tail_2}"
        print(f"脱敏结果: {result_2}")
        assert result_2 == "139****5678"

    # 测试 3: 长度不足
    phone_3 = "1380013800"
    print(f"长度: {len(phone_3)}")
    assert len(phone_3) != 11

    # 测试 4: 包含非数字字符
    phone_4 = "1380013800a"
    print(f"是否全数字: {phone_4.isdigit()}")
    assert phone_4.isdigit() is False

    print("所有测试通过!")
