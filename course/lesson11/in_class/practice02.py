# 测试区(教师可复制到终端验证)
# ======================
if __name__ == '__main__':
    # 测试 1: 多个参数
    print(f"测试1: {sum_all(1, 2, 3, 4, 5)}")

    # 测试 2: 单个参数
    print(f"测试2: {sum_all(99)}")

    # 测试 3: 无参数
    print(f"测试3: {sum_all()}")
