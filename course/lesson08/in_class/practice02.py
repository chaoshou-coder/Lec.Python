"""
[难度: ⭐⭐]
[所属知识点: 字符串replace()]
[预计完成时间: 10 分钟]

题目描述:
    输入一段文本和一个敏感词,用 replace 把敏感词替换成 ***,并打印结果。

示例:
    >>> text = "这个人很垃圾,真是太垃圾了"
    >>> word = "垃圾"
    输出: 这个人真是太***了,真是太***了

"""

# ======================
# 学员代码区(以 pass 作为占位符)
# ======================
text = input("请输入一段文本: ")
word = input("请输入敏感词: ")
result = text.replace(word, "***")
print("替换后:", result)

# ======================
# 测试区(教师可复制到终端验证)
# ======================
if __name__ == '__main__':
    # 测试 1: 敏感词出现多次
    t1, w1 = "这个人很垃圾,真是太垃圾了", "垃圾"
    print(f"测试1: {t1.replace(w1, '***')}")

    # 测试 2: 敏感词只出现一次
    t2, w2 = "你好坏", "坏"
    print(f"测试2: {t2.replace(w2, '***')}")

    # 测试 3: 敏感词不存在
    t3, w3 = "今天天气很好", "坏"
    print(f"测试3: {t3.replace(w3, '***')}")
