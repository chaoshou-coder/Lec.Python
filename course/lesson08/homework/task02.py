"""
[难度: ⭐⭐⭐⭐]
[所属知识点: 循环 + replace()]
[预计完成时间: 20 分钟]

题目描述:
    输入一段文本和一个敏感词列表,用循环 + replace 把所有敏感词
    替换成对应长度的 *,并打印结果。

示例:
    >>> text = "这个人很垃圾,太混蛋了"
    >>> words = ["垃圾","混蛋"]
    输出: 这个人真是太***了,太***了
"""

# ======================
# 学员代码区(以 pass 作为占位符)
# ======================
text = input("请输入一段文本: ")
n = int(input("请输入敏感词个数: "))
words = [input(f"敏感词 {i+1}: ") for i in range(n)]
for w in words:
    text = text.replace(w, "*" * len(w))
print("过滤后:", text)

