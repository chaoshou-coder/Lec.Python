"""
[难度: ⭐⭐⭐]
[所属知识点: in 运算符]
[预计完成时间: 15 分钟]

题目描述:
  让用户输入 3 个用户名,存入列表中。
  然后输入一个新的用户名,检查该用户名是否已存在。
  若存在,提示 "用户名已存在,请重新输入";
  若不存在,提示 "注册成功"。
  要求使用 in 运算符判断是否存在。

示例:
    >>> 已有用户名: ["张三", "李四", "王五"]
    >>> 新用户名: "李四"
    输出: 用户名已存在,请重新输入
    >>> 新用户名: "赵六"
    输出: 注册成功
"""

# ======================
# 学员代码区(以 pass 作为占位符)
# ======================
users = ["张三", "李四", "王五"]
new_user = input("请输入新用户名:")
if new_user in users:
    print("用户名已存在,请重新输入")
else:
    print("注册成功")

# ======================
# 测试区(教师可复制到终端验证)
# ======================
if __name__ == '__main__':
    # 测试 1: 用户名已存在
    users_1 = ["张三", "李四", "王五"]
    new_1 = "李四"
    if new_1 in users_1:
        result_1 = "用户名已存在,请重新输入"
    else:
        result_1 = "注册成功"
    print(result_1)
    assert result_1 == "用户名已存在,请重新输入"

    # 测试 2: 用户名不存在
    users_2 = ["张三", "李四", "王五"]
    new_2 = "赵六"
    if new_2 in users_2:
        result_2 = "用户名已存在,请重新输入"
    else:
        result_2 = "注册成功"
    print(result_2)
    assert result_2 == "注册成功"

    # 测试 3: 空字符串用户名
    users_3 = ["张三", "李四", "王五"]
    new_3 = ""
    if new_3 in users_3:
        result_3 = "用户名已存在,请重新输入"
    else:
        result_3 = "注册成功"
    print(result_3)
    assert result_3 == "注册成功"

    print("所有测试通过!")
