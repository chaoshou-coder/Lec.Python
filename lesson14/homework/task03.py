if __name__ == '__main__':
    import pandas as pd

    # 测试 1: 内连接合并
    df1 = pd.DataFrame({
        "员工ID": [1, 2, 3],
        "姓名": ["张三", "李四", "王五"],
        "部门ID": [10, 20, 30]
    })
    df2 = pd.DataFrame({
        "部门ID": [10, 20, 30],
        "部门名称": ["技术部", "市场部", "财务部"]
    })
    merged = pd.merge(df1, df2, on="部门ID")
    print(merged)
    assert list(merged["部门名称"]) == ["技术部", "市场部", "财务部"]

    # 测试 2: 检查行数
    assert len(merged) == 3

    # 测试 3: 检查列名
    assert "姓名" in merged.columns
    assert "部门名称" in merged.columns

    print("所有测试通过!")
