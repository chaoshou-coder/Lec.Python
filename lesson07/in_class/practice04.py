"""
[难度: ⭐⭐⭐⭐]
[所属知识点: Day04 嵌套列表 + Day05 循环 + Day06 分支]
[预计完成时间: 20 分钟]

题目描述:
  升级购物车功能! 现在购物车中每个商品是一个嵌套列表 [商品名, 数量]。
  当用户添加一个已存在的商品时,数量累加;否则新增一条记录。

功能菜单:
    1. 添加商品
    2. 查看购物车
    3. 删除商品
    0. 退出

操作说明:
    - 输入 1: 提示输入商品名和数量
      - 若商品已存在,数量累加
      - 若商品不存在,新增 [商品名, 数量]
    - 输入 2: 打印所有商品(编号、名称、数量)
    - 输入 3: 按编号删除商品
    - 输入 0: 退出

示例:
    >>> 请输入命令: 1
    >>> 请输入商品名称: 苹果
    >>> 请输入数量: 3
    >>> 苹果已添加到购物车!

    >>> 请输入命令: 1
    >>> 请输入商品名称: 苹果
    >>> 请输入数量: 2
    >>> 苹果数量已更新为 5!

    >>> 请输入命令: 2
    >>> 1. 苹果 - 数量: 5
"""

# ======================
# 学员代码区
# ======================

# 购物车: 每个元素是 [商品名, 数量]
cart = []

while True:
    cmd = input("请输入命令(1 添加/2 查看/3 删除/0 退出): ")

    if cmd == "1":
        name = input("请输入商品名称: ")
        qty = int(input("请输入数量: "))
        # 查找商品是否已存在
        found = False
        for item in cart:
            if item[0] == name:
                item[1] += qty
                found = True
                print(f"{name} 数量已更新为 {item[1]}!")
                break
        if not found:
            cart.append([name, qty])
            print(f"{name} 已添加到购物车!")

    elif cmd == "2":
        if len(cart) == 0:
            print("购物车为空!")
        else:
            for i in range(len(cart)):
                print(f"{i + 1}. {cart[i][0]} - 数量: {cart[i][1]}")

    elif cmd == "3":
        num = int(input("请输入要删除的编号: "))
        if 1 <= num <= len(cart):
            removed = cart.pop(num - 1)
            print(f"已删除: {removed[0]}")
        else:
            print("该编号不存在!")

    elif cmd == "0":
        print("欢迎下次光临!")
        break

    else:
        print("无效命令,请重新输入")

